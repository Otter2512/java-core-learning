public class App {
    public static void main(String[] args) throws Exception {
        int a = 5;
        boolean b = false; 
        System.out.println(" - => giá trị: "+ (-a));
        System.out.println(" + => giá trị: "+ (+a));
        System.out.println(" ! => giá trị: "+ (!b));

        System.out.println("-----------------------------------");

        System.out.println("a = "+ a);
        System.out.println("++a = "+ (++a));
        System.out.println("a++ = "+ (a++));
        System.out.println("a = "+ a);
        System.out.println("--a = "+ (--a));
        System.out.println("a-- = "+ (a--));
        System.out.println("a = "+ a);
    }
}
